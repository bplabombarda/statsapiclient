"""
Game play-by-play class.
"""

class PlayByPlay:
    """
    Gets play-by-play data.
    Args:
        :data: Raw play-by-play object
    """

    def __init__(self, data):
        self.data = data

    def do_a_thin(self):
        """Does a thin."""
        return self.data

    def do_another_thing(self):
        """Does another thing."""
        return self.data
